import {
  BaseBasicCardView,
  IBasicCardParameters,
  IExternalLinkCardAction,
  IQuickViewCardAction,
  ICardButton
} from '@microsoft/sp-adaptive-card-extension-base';
import * as strings from 'AsyncCardAdaptiveCardExtensionStrings';
import { IAsyncCardAdaptiveCardExtensionProps, IAsyncCardAdaptiveCardExtensionState, QUICK_VIEW_REGISTRY_ID } from '../AsyncCardAdaptiveCardExtension';

export class CardView extends BaseBasicCardView<IAsyncCardAdaptiveCardExtensionProps, IAsyncCardAdaptiveCardExtensionState> {
  public get cardButtons(): [ICardButton] | [ICardButton, ICardButton] | undefined {
    return [
      {
        title: strings.QuickViewButton,
        action: {
          type: 'QuickView',
          parameters: {
            view: QUICK_VIEW_REGISTRY_ID
          }
        }
      }
    ];
  }

  public get data(): IBasicCardParameters {
    return {
      primaryText: this.state.user.displayName
    };
  }

  public get onCardSelection(): IQuickViewCardAction | IExternalLinkCardAction | undefined {
    return {
      type: 'ExternalLink',
      parameters: {
        target: 'https://www.bing.com'
      }
    };
  }
}
